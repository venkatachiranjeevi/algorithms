class Solution:
    # @param A : list of list of integers
    # @return the same list modified
    def setZeroes(self, A):
        m = len(A)
        if m == 0:
            return A
        n = len(A[0])
        rows = [0] * m
        columns = [0] * n
        i = 0
        j = 0
        for x in range(m):
            for y in range(n):
                if A[x][y] == 0:
                    rows[x] = 1
                    columns[y] = 1
        for x in range(m):
            for y in range(n):
                if rows[x] == 1 or columns[y] == 1:
                    A[x][y] = 0
        return A


